+ (UIColor *)zb_colorWithRGB:(CGFloat)red green:(CGFloat)green blue:(CGFloat)blue alpha:(CGFloat)alpha
{
    return [self colorWithRed:red/255.f green:green/255.f blue:blue/255.f alpha:alpha];
}

+ (UIColor *)zb_colorWithHex:(long)hex
{
    CGFloat red, green, blue;
    blue = hex & 0xFF;
    green = (hex >> 8) & 0xFF;
    red = (hex >> 16) & 0xFF;
    return [self zb_colorWithRGB:red green:green blue:blue alpha:1.f];
}

+ (UIColor *)zb_colorWithHexString:(NSString *)hexString
{
    NSMutableCharacterSet *charactertSet = [NSMutableCharacterSet whitespaceCharacterSet];
    [charactertSet addCharactersInString:@"#"];
    hexString = [hexString stringByTrimmingCharactersInSet:charactertSet];
    unsigned int hexValue;
    [[NSScanner scannerWithString:hexString] scanHexInt:&hexValue];
    return [self zb_colorWithHex:hexValue];
}

- (long)zb_toHex
{
    CGFloat red, green, blue, alpha;
    [self getRed:&red green:&green blue:&blue alpha:&alpha];
    long hexValue = 0;
    hexValue += ((long)(red * 255)) << 16;
    hexValue += ((long)(green * 255)) << 8;
    hexValue += (long)(blue * 255);
    return hexValue;
}

- (NSString *)zb_toHexString
{
    long hexValue = [self zb_toHex];
    return [NSString stringWithFormat:@"%06lX", hexValue];
}


